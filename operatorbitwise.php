<?php
    $a = 60;
    $b = 13;

    $c = $a & $b;
    echo "$a & $b = $c";
    echo "<br>";

    $c = $a | $b;
    echo "$a | $b = $c";
    echo "<br>";

    $c = $a ^ $b;
    echo "$a ^ $b = $c";
    echo "<br>";

    $c = $a << $b;
    echo "$a << $b = $c";
    echo "<br>";

    $c = $a >> $b;
    echo "$a >> $b = $c";
    echo "<br>";
?>